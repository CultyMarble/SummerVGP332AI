#pragma once
#include "raylib.h"
#include "AI.h"

#include "Miner.h"

int main(void)
{
    /*==================================================================================
        INITIALIZATION
    ==================================================================================*/
    // Window & Devices
    const int screenWidth = 300;
    const int screenHeight = 50;

    InitWindow(screenWidth, screenHeight, "VGP332 - AI");
    SetTargetFPS(144);

    /*==================================================================================
        LOAD RESOURCES
    ==================================================================================*/


    /*==================================================================================
        GAME OBJECT
    ==================================================================================*/
    Miner miner;

    /*==================================================================================
        GAME INIT
    ==================================================================================*/
    miner.Initialize();


    // Main game loop
    while (!WindowShouldClose())    // Detect window close button or ESC key
    {
        /*==================================================================================
            UPDATE GAME FRAME
        ==================================================================================*/
        if (IsKeyPressed(KEY_SPACE))
        {
            miner.Update();
        }

        /*==================================================================================
            UPDATE DRAW FRAME
        ==================================================================================*/
        BeginDrawing();
        ClearBackground(GRAY);

        EndDrawing();
    }

    /*==================================================================================
        DE-INITIALIZATION
    ==================================================================================*/
    miner.Terminate();

    CloseWindow();
    return EXIT_SUCCESS;
}